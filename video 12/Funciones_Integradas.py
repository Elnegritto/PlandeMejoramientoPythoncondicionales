# Funciones integradas

n = str(10.98)

print(n)

# Valor de entero a binario

n = bin(10)

print(n)

# Valor Hexadecimal

n = hex(10)

print(n)

# Valor de binario a entero 

n = int("0b1010",2)

print(n)

# Valor de hexadecimal a entero

n = int("0xa",16)

print(n)

# Valor absoluto de un numero 

n = abs(-8)

print(n)

# Funcion para redondiar un numero

n = round(5.4)

print(n)

# Funcion para contar cuantos caracteres tiene la cadena 

n = len("Kevin")

print(n) 


